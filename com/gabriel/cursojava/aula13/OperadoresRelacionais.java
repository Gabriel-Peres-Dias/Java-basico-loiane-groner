package com.gabriel.cursojava.aula13;

public class OperadoresRelacionais {
        
    public static void main(String[] args) {

        int valor1 = 1;
        int valor2 = 2;

        // Comparações utilizando operadores relacionais
        System.out.println("Valor 1 == Valor 2: " + (valor1 == valor2));
        System.out.println("Valor 1 != Valor 2: " + (valor1 != valor2));
        System.out.println("Valor 1 >  Valor 2: " + (valor1  > valor2));
        System.out.println("Valor 1 >= Valor 2: " + (valor1 >= valor2));
        System.out.println("Valor 1 <  Valor 2: " + (valor1  < valor2));
        System.out.println("Valor 1 <= Valor 2: " + (valor1 <= valor2));
    }
}
